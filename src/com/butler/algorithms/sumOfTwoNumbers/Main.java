//        Create a method that takes two integers as arguments and returns their sum.
//
//        Examples
//        SumOfTwoNumbers(3, 2) ➞ 5
//
//        SumOfTwoNumbers(-3, -6) ➞ -9
//
//        SumOfTwoNumbers(7, 3) ➞ 10

public class Main {

    public static void main(String[] args) {
        // Output to test functionality
        System.out.println(sumOfNumbers(-3, -6));
    }

    public static Integer sumOfNumbers(int a, int b) {
        // Return sum of both numbers
        return a + b;
    }
}
